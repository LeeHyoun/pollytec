package sinsung.sinsungDTO;

public class StConditionDTO {
	String firstCondition;
	String secondCondition;
	String thirdCondition;
	String rank;
	String firstDate;
	String lastDate;
	String searchCondition;
	String searchContent;
	
	@Override
	public String toString() {
		return "StConditionDTO [firstCondition=" + firstCondition
				+ ", secondCondition=" + secondCondition + ", thirdCondition="
				+ thirdCondition + ", rank=" + rank + ", firstDate=" + firstDate
				+ ", lastDate=" + lastDate + ", searchCondition="
				+ searchCondition + ", searchContent=" + searchContent + "]";
	}
	public String getFirstCondition() {
		return firstCondition;
	}
	public void setFirstCondition(String firstCondition) {
		this.firstCondition = firstCondition;
	}
	public String getSecondCondition() {
		return secondCondition;
	}
	public void setSecondCondition(String secondCondition) {
		this.secondCondition = secondCondition;
	}
	public String getThirdCondition() {
		return thirdCondition;
	}
	public void setThirdCondition(String thirdCondition) {
		this.thirdCondition = thirdCondition;
	}
	public String getRank() {
		return rank;
	}
	public void setRank(String rank) {
		this.rank = rank;
	}
	public String getFirstDate() {
		return firstDate;
	}
	public void setFirstDate(String firstDate) {
		this.firstDate = firstDate;
	}
	public String getLastDate() {
		return lastDate;
	}
	public void setLastDate(String lastDate) {
		this.lastDate = lastDate;
	}
	public String getSearchCondition() {
		return searchCondition;
	}
	public void setSearchCondition(String searchCondition) {
		this.searchCondition = searchCondition;
	}
	public String getSearchContent() {
		return searchContent;
	}
	public void setSearchContent(String searchContent) {
		this.searchContent = searchContent;
	}
}
